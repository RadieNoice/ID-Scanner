package com.ocr.id.dto;

public class Membersdto {
	
	private String regnum;
	private String name;
	private String college_department;
	private String vit_email;
	private String club_dept;
	private String mobile_no;
	private String role;
	
	
	public String getRole() {
		return role;
	}
	public void setRole(String role) {
		this.role = role;
	}
	public String getRegnum() {
		return regnum;
	}
	public void setRegnum(String regnum) {
		this.regnum = regnum;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCollege_department() {
		return college_department;
	}
	public void setCollege_department(String college_department) {
		this.college_department = college_department;
	}
	public String getVit_email() {
		return vit_email;
	}
	public void setVit_email(String vit_email) {
		this.vit_email = vit_email;
	}
	public String getClub_dept() {
		return club_dept;
	}
	public void setClub_dept(String club_dept) {
		this.club_dept = club_dept;
	}
	public String getMobile_no() {
		return mobile_no;
	}
	public void setMobile_no(String mobile_no) {
		this.mobile_no = mobile_no;
	}
	
	

}
